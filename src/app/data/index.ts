export interface Program {
  id: string;
  title: string;
  subtitle: string;
  description: string;
  duration: string;
  format: string;
  level: string;
  start?: string;
  modules: string[];
  result: string;
}

export interface Book {
  id: string;
  title: string;
  author: string;
  year: string;
  description: string;
  category: 'book' | 'article' | 'dissertation';
  fileUrl?: string;
  pages?: number;
}

export interface Event {
  id: string;
  title: string;
  date: string;
  location: string;
  description: string;
  type: string;
}

export interface Review {
  id: string;
  author: string;
  role: string;
  text: string;
  date: string;
  type: 'written' | 'video';
  videoUrl?: string;
  videoThumb?: string;
  city?: string;
}

export interface FAQ {
  id: string;
  question: string;
  answer: string;
}

